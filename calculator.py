number_1 = float(input("Enter your first number:"))
operation = input("Enter your operation:")
number_2 = float (input("Enter your second number:"))

if operation == "+":
    print(number_1 + number_2)
elif operation == "-":
    print(number_1 - number_2)
elif operation == "*":
        print(number_1 * number_2)
elif operation == "/":
        print(number_1 / number_2)
else:
        print("Invalid operation")
                    # The code above is a calculator that takes two numbers and an operation as input and returns the result of the operation.                          
